import UIKit

extension UIFont {
    
    static func interSemiBold10() -> UIFont? {
        return UIFont.init(name: "Inter-SemiBold", size: 10)
    }
    
    static func interSemiBold14() -> UIFont? {
        return UIFont.init(name: "Inter-SemiBold", size: 14)
    }
    
    static func gillSans50() -> UIFont? {
        return UIFont.init(name: "gillSans", size: 50)
    }
}

