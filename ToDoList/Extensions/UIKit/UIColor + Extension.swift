import UIKit

extension UIColor {
    
    static func redColor() -> UIColor {
        return #colorLiteral(red: 0.989353478, green: 0.02273192257, blue: 0.1401548088, alpha: 1)
    }
    
    static func blueColor() -> UIColor {
        return #colorLiteral(red: 0.2987577915, green: 0.4544386268, blue: 1, alpha: 1)
    }

    static func darkColor() -> UIColor {
        return #colorLiteral(red: 0.1600292623, green: 0.1647125483, blue: 0.1862020195, alpha: 1)
    }
    
    static func lightColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func grayColor() -> UIColor {
        return #colorLiteral(red: 0.8039215803, green: 0.8039215803, blue: 0.8039215803, alpha: 1)
    }
}

